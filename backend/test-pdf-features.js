/**
 * Direct PDF Generation Test
 * Tests PDF rendering with proper footers, page numbers, and AI reports
 */

const PDFDocument = require('pdfkit');
const fs = require('fs');
const path = require('path');

async function testPDFRendering() {
  try {
    console.log('🧪 Testing PDF Rendering with All Features\n');

    // Create PDF document with proper settings
    const doc = new PDFDocument({
      size: 'A4',
      margin: 50,
      bufferPages: false
    });

    // Test file path
    const pdfPath = path.join(__dirname, 'TEST-PDF-FEATURES.pdf');
    const writeStream = fs.createWriteStream(pdfPath);
    doc.pipe(writeStream);

    let pageCount = 1;

    // Helper: Render footer
    const renderFooter = () => {
      const pageHeight = doc.page.height;
      const pageWidth = doc.page.width;
      const footerY = pageHeight - 40;
      
      // Separator line
      doc.strokeColor('#cccccc').lineWidth(0.5);
      doc.moveTo(50, footerY).lineTo(pageWidth - 50, footerY).stroke();
      doc.strokeColor('#000000');
      
      // Footer text  
      doc.fontSize(6).font('Times-Roman').fillColor('#666666');
      doc.text(`Document Control: F-TST-001 V26.1 | Generated: ${new Date().toLocaleDateString()}`, 
        50, footerY + 5, { width: pageWidth - 100, align: 'center' });
      
      // Page number
      doc.text(`Page ${pageCount}`, pageWidth - 100, footerY + 5, { width: 50, align: 'right' });
      
      doc.fillColor('#000000');
    };

    // Helper: Check page break
    const checkPageBreak = (spaceNeeded = 120) => {
      const pageHeight = doc.page.height;
      const bottomMargin = 50;
      const footerHeight = 75;
      const contentBottomLimit = pageHeight - bottomMargin - footerHeight;

      if (doc.y + spaceNeeded > contentBottomLimit) {
        renderFooter();
        doc.addPage();
        pageCount++;
        
        // Header for continuation
        doc.y = 45;
        doc.fontSize(8).font('Times-Roman').fillColor('#666666');
        doc.text('(Continued) Form ID: TEST-001', { align: 'center' });
        doc.fillColor('#000000');
        doc.moveDown(0.4);
        
        return true;
      }
      return false;
    };

    // PAGE 1: Title and Header
    doc.fontSize(24).font('Helvetica-Bold').fillColor('#1e40af').text('Test Safety Report', { align: 'center' });
    doc.fillColor('#000000');
    doc.moveDown(0.3);

    doc.fontSize(9).font('Times-Roman').fillColor('#666666');
    doc.text('Form ID: TEST-001 | Submitted: 2026-02-19', { align: 'center' });
    doc.fillColor('#000000');
    doc.moveDown(0.5);

    doc.strokeColor('#1e40af').lineWidth(1);
    doc.moveTo(50, doc.y).lineTo(545, doc.y).stroke();
    doc.moveDown(0.5);
    doc.strokeColor('#000000');

    // AI Report Section
    checkPageBreak(180);
    
    doc.fill('#e0f2fe').rect(50, doc.y, 495, 25).fill();
    doc.fontSize(13).font('Times-Bold').fillColor('#0369a1').text('SAFETY ANALYSIS REPORT', 65, doc.y + 5);
    doc.moveUp(0.5).moveDown(0.8);
    
    doc.strokeColor('#0369a1').lineWidth(2.5);
    doc.moveTo(50, doc.y - 4).lineTo(545, doc.y - 4).stroke();
    doc.strokeColor('#000000');
    doc.moveDown(0.5);
    
    doc.fontSize(10).font('Times-Roman').fillColor('#000000');
    const aiReport = "This is a comprehensive safety analysis. The identified hazards include sharp cutting equipment, wet floor conditions, and temperature extremes. Recommended control measures include proper training, non-slip flooring, and protective equipment. Risk assessment indicates moderate risk with proper controls in place.";
    doc.text(aiReport, { width: 495, align: 'left', lineGap: 3, paragraph: 0.5 });
    
    doc.moveDown(0.5);
    doc.fontSize(7).font('Times-Italic').fillColor('#667780');
    doc.text('Generated by Claude AI • 2026-02-19 14:30:00', { align: 'right' });
    doc.fillColor('#000000');
    doc.moveDown(1);

    // Job Steps Table
    checkPageBreak(240);

    doc.moveDown(0.8);
    doc.fill('#e8f0fe').rect(50, doc.y, 495, 26).fill();
    doc.fontSize(13).font('Times-Bold').fillColor('#1e40af').text('JOB STEPS', 65, doc.y + 5);
    doc.moveUp(0.5).moveDown(0.8);
    doc.strokeColor('#1e40af').lineWidth(2.5).moveTo(50, doc.y - 4).lineTo(545, doc.y - 4).stroke();
    doc.strokeColor('#000000');
    doc.moveDown(1);

    // Table header
    const headerY = doc.y;
    doc.fill('#e8f0fe').rect(50, headerY, 495, 24).fill();
    doc.strokeColor('#d0d5dd').lineWidth(1).rect(50, headerY, 495, 24).stroke();
    
    doc.fontSize(9).font('Times-Bold').fillColor('#1e40af');
    doc.text('#', 58, headerY + 6);
    doc.text('JOB STEP', 140, headerY + 6);
    doc.text('HAZARDS', 300, headerY + 6);
    doc.text('CONTROLS', 430, headerY + 6);
    
    doc.y = headerY + 24;
    doc.moveDown(0.3);

   // Test generating multiple pages by adding content
    for (let i = 1; i <= 15; i++) {
      if (checkPageBreak(30)) {
        // Redraw table header on new page
        const hY = doc.y;
        doc.fill('#e8f0fe').rect(50, hY, 495, 24).fill();
        doc.strokeColor('#d0d5dd').lineWidth(1).rect(50, hY, 495, 24).stroke();
        
        doc.fontSize(9).font('Times-Bold').fillColor('#1e40af');
        doc.text('#', 58, hY + 6);
        doc.text('JOB STEP', 140, hY + 6);
        doc.text('HAZARDS', 300, hY +6);
        doc.text('CONTROLS', 430, hY + 6);
        
        doc.y = hY + 24;
        doc.moveDown(0.3);
      }

      // Table rows with alternating backgrounds
      const rowY = doc.y;
      const rowHeight = 25;
      
      if (i % 2 === 1) {
        doc.fill('#f8f9fa').rect(50, rowY, 495, rowHeight).fill();
      }
      
      doc.strokeColor('#d0d5dd').lineWidth(1).rect(50, rowY, 495, rowHeight).stroke();
      
      doc.fill('#000000').font('Times-Roman').fontSize(8);
      doc.text(String(i), 58, rowY + 6);
      doc.text(`Step ${i}`, 140, rowY + 6);
      doc.text(`Hazard ${i}`, 300, rowY + 6);
      doc.text(`Control ${i}`, 430, rowY + 6);
      
      doc.y = rowY + rowHeight;
    }

    doc.moveDown(1);

    // Add final footer
    renderFooter();

    // Finish PDF
    doc.end();

    writeStream.on('finish', () => {
      const stats = fs.statSync(pdfPath);
      console.log(`✅ Test PDF created: ${pdfPath} (${stats.size} bytes)\n`);
      
      console.log('📋 Features tested:');
      console.log('  ✅ Footer with document control number');
      console.log('  ✅ Page numbers');
      console.log('  ✅ AI Report section with proper formatting');
      console.log('  ✅ Professional table with headers and rows');
      console.log('  ✅ Table header repetition on page breaks');
      console.log('  ✅ Alternating row colors');
      console.log('  ✅ Multiple page generation');
      console.log(`\n📄 Total pages generated: ${pageCount}`);
      console.log('\n✨ PDF rendering test completed successfully!');
    });

  } catch (error) {
    console.error('❌ Test failed:', error);
    process.exit(1);
  }
}

testPDFRendering();
